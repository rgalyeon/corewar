#include "../../includes/libft.h"

void	ft_swaps(void *a, void *b, size_t size)
{
	u_int8_t tmp;

	while (size--)
	{
		tmp = *(u_int8_t *)a;
		*(u_int8_t *)a = *(u_int8_t *)b;
		*(u_int8_t *)b = tmp;
		a++;
		b++;
	}
}
