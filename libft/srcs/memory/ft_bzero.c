#include "../../includes/memory_ft.h"

void	ft_bzero(void *s, size_t n)
{
	register char	*b;
	register size_t	l;

	b = (char *)s;
	if (n != 0)
	{
		l = n;
		while (l > 4)
		{
			b[0] = '\0';
			b[1] = '\0';
			b[2] = '\0';
			b[3] = '\0';
			l -= 4;
			b += 4;
		}
		while (l--)
			*b++ = '\0';
	}
}
