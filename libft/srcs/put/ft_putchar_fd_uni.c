#include "../../includes/libft.h"

void	ft_putchar_fd_uni(int c, int fd)
{
	char d[4];

	if (c < 127)
		write(fd, &c, 1);
	if (c >= 128 && c <= 2047)
	{
		d[0] = 192 + c / 64;
		d[1] = 128 + c % 64;
		write(fd, d, 2);
	}
	else if (c >= 2048 && c <= 65535)
	{
		d[0] = 224 + (c / 4096);
		d[1] = 128 + ((c / 64) % 64);
		d[2] = 128 + (c % 64);
		write(fd, d, 3);
	}
	else if (c >= 65536 && c <= 2097151)
	{
		d[0] = 240 + (c / 262144);
		d[1] = 128 + ((c / 4096) % 64);
		d[2] = 128 + ((c / 64) % 64);
		d[3] = 128 + (c % 64);
		write(fd, d, 4);
	}
}
