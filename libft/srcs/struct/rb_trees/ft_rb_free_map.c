#include "../../../includes/rb_trees_ft.h"

void	ft_rb_free_map(t_map **map)
{
	if ((*map)->size == 0)
	{
		free((*map)->nil);
		(*map)->nil = NULL;
		free(*map);
		*map = NULL;
	}
}
