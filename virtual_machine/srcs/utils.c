#include "virtual_machine.h"

/*
** Reverse bytes in 64 bit integer
*/

uint	reverse_bytes(uint n)
{
	n = (n >> 16u) | (n << 16u);
	n = (n & 0xff00ff00u) >> 8u | (n & 0x00ff00ffu) << 8u;
	return (n);
}
